				<h3>Fase de repouso</h3>

				<p>Na fase de repouso, é administrado o radiofármaco no paciente (em geral, injetável, mas sendo possível também por via oral). Os radiofármacos são substâncias da área da medicina nuclear, que se fixam em áreas específicas do corpo, ajudando a identificar alterações nas áreas estudadas.</p>

				<p>No caso da cintilografia miocárdica, o radiofármaco mais utilizado é o SESTAMIBI 99mTc. Ao ser aplicado no paciente, esse medicamento se concentra no músculo cardíaco, para que o aparelho possa fazer a captação das imagens.</p>

				<p>Após a aplicação, é necessário aguardar de 30 a 40 minutos para que o material se concentre no coração, e então as primeiras imagens são coletadas.</p>

				<p>Depois disso, é iniciada a segunda fase do exame: a fase de estresse.</p>

				<h3>Fase de estresse</h3>

				<p>Na fase de estresse, o paciente precisa ser submetido a uma situação de esforço. Pode ser <strong>esforço físico</strong> – através de um teste ergométrico – ou <strong>esforço farmacológico</strong> – quando esse estresse é provocado com o uso de medicamentos.</p>

				<p>Assim que a frequência cardíaca do paciente atinge o nível desejado para sua idade, é aplicada a segunda dose de
				radiofármaco. Novamente é preciso aguardar de 30 a 40 minutos para que se faça a coleta de imagens do coração em estresse.</p>

				<p>Por fim, o médico faz a comparação entre as duas imagens – em repouso e em estresse – para avaliar a saúde cardíaca do
				paciente.</p>

				<h3>Preparo</h3>

				<p>O preparo para o exame de cintilografia miocárdica tem relação com a chamada fase de estresse.</p>

				<p>Para realizar o exame, é importante que o paciente faça repouso no dia anterior e, caso seja fumante, interrompa o consumo nas últimas 24 horas. Alguns medicamentos também poderão ser suspensos, de acordo com a orientação médica.</p>

				<p>Em relação aos cuidados com a alimentação, <strong>deve-se evitar a ingestão de certos alimentos 24 horas antes do exame</strong>, tais como:</p>

				<div class="lista">
				<p><strong>Café ou bebidas à base de cafeína.</strong></p>
				<p><strong>Chás estimulantes.</strong></p>
				<p><strong>Chocolate.</strong></p>
				<p><strong>Refrigerante.</strong></p>
				</div>

				<p>Também é aconselhado que se faça uma <strong>refeição leve não menos que 2 horas</strong> antes do exame, e compareça ao local com <strong>roupa confortável</strong> e <strong>tênis</strong> para realizar o exercício da fase de estresse.</p>

				<h3>Indicações e contraindicações</h3>

				<p>Em geral, a solicitação da cintilografia miocárdica é feita pelo médico para tratamento de pacientes que já têm histórico de doenças no coração ou quando há suspeita de cardiopatias.</p>

				<p>Por isso, este exame é indicado quando o paciente apresenta, entre outros casos:</p>

				<div class="lista">
				<p><strong>Fatores de risco para doenças cardíacas.</strong></p>
				<p><strong>Alterações no eletrocardiograma.</strong></p>
				<p><strong>Sintomas de infarto.</strong></p>
				<p><strong>Insuficiência cardíaca.</strong></p>
				<p><strong>Coração transplantado.</strong></p>
				</div>

				<h3>Possíveis riscos</h3>

				<p>Se a fase de estresse for feita com uso de medicamentos (esforço farmacológico), é possível que o paciente sinta alguns efeitos colaterais, como leve dor no peito, enjoo, tontura e alteração da pressão arterial. No entanto, esses sintomas são raros e, quando ocorrem, costumam passar em poucos instantes.</p>

				<p>Algumas pessoas podem ficar com receio pelo fato da cintilografia miocárdica fazer uso de substâncias como os radiofármacos. Mas não é preciso se preocupar. As doses aplicadas no paciente são pequenas e controladas, e a quantidade de radiação é mínima e rapidamente eliminada pelo corpo. Tudo é feito de maneira muito segura.</p>

				<p>Além disso, é preciso lembrar que o exame é <strong>contraindicado para pacientes grávidas ou em fase de amamentação.</strong> No entanto, é possível a essas pacientes fazerem o exame, de acordo com a avaliação do seu médico-assistente e do radiologista.</p>

