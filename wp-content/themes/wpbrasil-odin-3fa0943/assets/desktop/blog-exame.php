				<h1>Conheça as vantagens da cintilografia miocárdica!</h1>
				<p>As doenças do coração são a principal causa de morte no
				Brasil. Infarto, AVC, morte súbita e outras doenças
				cardíacas causam mais de 1.100 óbitos por dia no país,
				<a>segundo dados</a> da Sociedade Brasileira de Cardiologia.</p>

				<p>Com esses números em mãos, é fácil entender a
				importância que se deve dar aos exames preventivos e ao
				tratamento adequado das cardiopatias. Por isso que a
				cardiologia foi uma das áreas médicas que mais
				avançaram nos últimos anos.</p>

				<p>E dentre os exames que investigam as doenças do coração, um dos mais destacados e completos é a cintilografia miocárdica.</p>

				<p>Esse exame é capaz de avaliar o funcionamento do coração e potenciais falhas no músculo cardíaco, através de imagens
				altamente precisas, sendo um exame fundamental para confirmar suspeitas e esclarecer diagnósticos.</p>

				<p>Neste artigo você vai conhecer como é feito o exame de cintilografia miocárdica e quais as suas maiores vantagens.</p>

				<h3>O que é cintilografia miocárdica? Quais as suas vantagens?</h3>

				<p>A cintilografia miocárdica é um exame de diagnóstico por imagem, minimamente invasivo, cuja finalidade é <strong>avaliar o fluxo	sanguíneo nas artérias do coração.</strong></p>

				<p>Também conhecido como cintilografia de perfusão do miocárdio, este exame busca detectar possíveis <strong>falhas na irrigação do	músculo cardíaco.</strong></p>

				<p>Por ser extremamente eficaz e de fácil execução, a cintilografia do miocárdio se torna uma opção, na fase em que há alguma suspeita de cardiopatia, para substituir outros exames mais invasivos, como o cateterismo.</p>

				<p>As imagens precisas e de excelente resolução geradas pela cintilografia auxiliam o médico a obter um diagnóstico mais seguro e assim direcionar o tratamento de maneira específica para várias doenças, tais como:</p>
				<div class="lista">
				<p><strong>Doença arterial coronariana –</strong> Obstrução total ou parcial de uma ou mais artérias coronárias por placas de gordura e coágulos.</p>
				<p><strong>Isquemia miocárdica –</strong> Diminuição da passagem de sangue pelas artérias.</p>
				<p><strong>Insuficiência cardíaca –</strong> Quando o coração não consegue mais bombear sangue suficiente para o resto do corpo.</p>
				<p><strong>Arritmia cardíaca –</strong> Alterações no ritmo dos batimentos cardíacos, seja aceleração ou redução.</p>
				<p><strong>Fibrose miocárdica –</strong> Necrose de partes do músculo cardíaco.</p>
				</div>

				<h3>Como é feito o exame de cintilografia miocárdica?</h3>

				<p>Para obter imagens com alto nível de precisão, o exame de cintilografia miocárdica faz a <strong>junção de dois elementos</strong> essenciais: o <strong>radiofármaco</strong>, que é aplicado no paciente, e o aparelho de câmara de cintilação, também chamado de <strong>gama-câmara.</strong></p>

				<p>O procedimento é dividido em duas fases: <strong>a fase de repouso e a fase estresse.</strong> A depender da clínica em que o exame é feito, essas fases podem ser feitas no mesmo dia (protocolo de 1 dia) ou em dois dias diferentes (protocolo de 2 dias).</p>

				<p><strong>Aqui no IMEB, realizamos o exame completo no mesmo dia!</strong></p>

